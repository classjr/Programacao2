    private void jButtonPesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonPesquisarMouseClicked
        this.controller.setFornecedor(null);

        if (!this.jTextFieldId.getText().equals("")) {
            this.controller.buscaFornecedorId(this.jTextFieldId.getText());
        } else if (!this.jTextFieldNome.getText().equals("")) {
            this.controller.buscaFornecedorNome(this.jTextFieldNome.getText());
        }

        if (this.controller.getFornecedor() == null) {
            this.jDialogFornecedorNaoEncontrado.setVisible(true);
            this.jDialogFornecedorNaoEncontrado.setLocationRelativeTo(null);
        } else {
            this.jTextFieldNomeFornecedor.setText(this.controller.getFornecedor().getNome());
            this.jFormattedTextFieldCNPJ.setText(this.controller.getFornecedor().getCnpj());

            this.atualizaTelefones();
            this.atualizaEmails();
            this.atualizaEnderecos();
        }
    }//GEN-LAST:event_jButtonPesquisarMouseClicked

